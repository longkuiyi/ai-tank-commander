export function getLanguage(): Language {
  const stored = (typeof localStorage !== 'undefined' && localStorage.getItem(STORAGE_KEY)) || '';
  const validLanguages: Language[] = ['zh-CN', 'en-US', 'ja-JP', 'ko-KR', 'fr-FR', 'de-DE', 'es-ES', 'pt-PT', 'ru-RU', 'it-IT'];
  if (validLanguages.includes(stored as Language)) return stored as Language;
  return 'zh-CN';
}

export function setLanguage(lang: Language) {
  try {
    localStorage.setItem(STORAGE_KEY, lang);
  } catch {
    // ignore
  }

  if (typeof document !== 'undefined') {
    document.documentElement.lang = lang;
  }
  window.dispatchEvent(new CustomEvent('languageChange', { detail: lang }));
}

export function t(key: string): string {
  const lang = getLanguage();
  return translations[lang][key] ?? translations['zh-CN'][key] ?? key;
}

